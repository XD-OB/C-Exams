#include <unistd.h>

int		main(int argc, char **argv)
{
	if (argc == 2)
	{
		while (*argv[1])
		{
			if (*argv[1] > 96 && *argv[1] < 123)
				*argv[1] = (((*argv[1] - 97) + 13) % 26) + 97;
			if (*argv[1] > 64 && *argv[1] < 91)
				*argv[1] = (((*argv[1] - 65) + 13) % 26) + 65;
			write(1, argv[1]++, 1);
		}
	}
	write(1, "\n", 1);
	return (0);
}
